export function getTemplatesByCategory(category: string): ToolboxTopic[] {
  return toolboxMeetingTemplates.filter((t) => t.category === category);
}

export function searchToolboxTemplates(query: string): ToolboxTopic[] {
  const q = query.toLowerCase();
  return toolboxMeetingTemplates.filter(
    (t) =>
      t.title.toLowerCase().includes(q) ||
      t.description.toLowerCase().includes(q) ||
      t.category.toLowerCase().includes(q)
  );
}
